#include "SonarSR04.h"

//------------------------------------------------------------------------------
// Конструктор
//------------------------------------------------------------------------------
SonarSR04::SonarSR04(uint8_t echoPin, 
                     uint8_t trigPin, 
                     uint32_t trigPulseUs, 
                     uint32_t preDelayUs)
    : trigPin(trigPin)
    , echoPin(echoPin)
    , trigPulseUs(trigPulseUs)
    , preDelayUs(preDelayUs)
    , lastStatus(0)
{
    // Тело конструктора пустое, инициализация через список инициализации
}

//------------------------------------------------------------------------------
// Инициализация пинов
//------------------------------------------------------------------------------
void SonarSR04::init()
{
    pinMode(trigPin, OUTPUT);
    pinMode(echoPin, INPUT);
    digitalWrite(trigPin, LOW);
}

//------------------------------------------------------------------------------
// Измерение расстояния
// Возвращает расстояние в сантиметрах
// Формула: расстояние (см) = время (мкс) / 58
// (звук проходит 1 см за ~58 мкс в обе стороны)
//------------------------------------------------------------------------------
float SonarSR04::getDistance()
{
    // --- 1. Формирование триггер-импульса ---
    digitalWrite(trigPin, LOW);
    delayMicroseconds(preDelayUs);      // Пауза перед импульсом
    
    digitalWrite(trigPin, HIGH);
    delayMicroseconds(trigPulseUs);     // Длительность импульса (рекомендуется 10 мкс)
    digitalWrite(trigPin, LOW);

    // --- 2. Ожидание эхо-сигнала ---
    // pulseIn() возвращает длительность импульса в микросекундах
    // или 0 при таймауте
    uint32_t echoDurationUs = pulseIn(echoPin, HIGH, ECHO_TIMEOUT_US);
    
    // --- 3. Проверка таймаута и вычисление расстояния ---
    if (echoDurationUs == 0)
    {
        lastStatus = 1;  // Ошибка таймаута
        return 0.0f;
    }
    
    lastStatus = 0;      // Успешное измерение
    
    // Расстояние (см) = время (мкс) / 58
    // Коэффициент 58 = 2 * 29 (скорость звука ~343 м/с = 29 мкс/см в одну сторону)
    return (float)echoDurationUs / 5.8f;
}